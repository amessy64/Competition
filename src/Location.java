
public class Location {
	private int aI;
	private int aJ;
	
	public Location(int i, int j)
	{
		setI(i);
		setJ(j);
	}

	public int getJ() {
		return aJ;
	}

	public void setJ(int pJ) {
		this.aJ = pJ;
	}

	public int getI() {
		return aI;
	}

	public void setI(int pI) {
		this.aI = pI;
	}
}
